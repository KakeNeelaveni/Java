package com.apjfsa;

public class DemoDoWhile {

	public static void main(String[] args) {
		int num=10;
		//do while loop
		do {
			System.out.println("Do While Loop");
			num--;
		}while(num>10);
		
		//while loop
		int num1=10;
	    while(num1>10)
	    {
	    	System.out.println(num1);
	    	num--;
	    }

	}

}
