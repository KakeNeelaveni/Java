package com.apjfsa;

public class RelationalDemo {

	public static void main(String[] args) {
		int a,b;
		a=50;
		b=40;
	    System.out.println("a==b is"+(a==b));
	    System.out.println("a!=b is"+(a!=b));
	    System.out.println("a<b is"+(a<b));
	    System.out.println("a<=b is"+(a<=b));
	    System.out.println("a>b is"+(a>b));
	    System.out.println("a>=b is"+(a>=b));
	    

	}

}
