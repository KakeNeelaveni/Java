package com.apjfsa;

public class SquareAndCube {
	//main method
    public static void main(String[] args) {
        
     

        // Calculate and output square and cube
        for (int i=2;i<=4;i++) {
            int square = i * i;//for squares
            int cube = i * i * i;//for cubes
            System.out.println(square);
            System.out.println(cube);
        }
    }
}
