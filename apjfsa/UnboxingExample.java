package com.apjfsa;

public class UnboxingExample {

	public static void main(String[] args) {
		
		Integer i=new Integer(30);
		
		int a=i;
		
		System.out.println(a);
		
		int b=12;
		
		Integer b1=new Integer(b);
		
		Integer b2=4;
		
		System.out.println(b1+" "+b2);
		

	}

}
